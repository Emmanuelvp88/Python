print("*******************************************")
print("*Programa que determina si un numero es par o inpar*")
print("*******************************************")

num1 = int(input ("Por favor ingresa un numero: "))


if num1 % 2 == 0:
    print("El numero ", num1, " es el que ingresaste es un numero par. \nFin.")
else:
    print("El numero ", num1, " es el que ingresaste es un numero inpar. \nFin.")    
