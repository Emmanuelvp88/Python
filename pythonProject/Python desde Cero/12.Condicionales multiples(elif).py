# CONDICIONALES MULTIPLES (elif) = (else if)

numero = int(input("Cual es el numero que deseas convertir: "))

if numero == 1:
    print("El numero es 'Uno'")
elif numero == 2:
    print("El numero es 'Dos'")
elif numero == 3:
    print("El numero es 'Tres'")
elif numero == 4:
    print("El numero es 'Cuatro'")
else:
    print("Este programa solo puede convertir Hasta el numero 4'Cuatro' ")
